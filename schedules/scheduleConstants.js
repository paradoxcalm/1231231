// Генерация массива тайм‑слотов на сутки: 00:00–01:00, 01:00–02:00 и т. д.
// Этот массив пригодится в формах создания/редактирования расписаний.
window.TIME_SLOTS = Array.from({ length: 24 }, (_, i) => {
    const start = String(i).padStart(2, "0");
    const end = String((i + 1) % 24).padStart(2, "0");
    return `${start}:00-${end}:00`;
});
